package myPackage;

public class City {
	String name;
	double latitude;
	double longitude;
	double costIndex;
	double crimeIndex;
	double pollutionIndex;
	double healthIndex;
	double propertyIndex;
	
	public City() {
	
	}
	
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the latitude
	 */
	public double getLatitude() {
		return latitude;
	}

	/**
	 * @param latitude the latitude to set
	 */
	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}

	/**
	 * @return the longitude
	 */
	public double getLongitude() {
		return longitude;
	}

	/**
	 * @param longitude the longitude to set
	 */
	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}

	/**
	 * @return the costIndex
	 */
	public double getCostIndex() {
		return costIndex;
	}

	/**
	 * @param costIndex the costIndex to set
	 */
	public void setCostIndex(double costIndex) {
		this.costIndex = costIndex;
	}

	/**
	 * @return the crimeIndex
	 */
	public double getCrimeIndex() {
		return crimeIndex;
	}

	/**
	 * @param crimeIndex the crimeIndex to set
	 */
	public void setCrimeIndex(double crimeIndex) {
		this.crimeIndex = crimeIndex;
	}

	/**
	 * @return the pollutionIndex
	 */
	public double getPollutionIndex() {
		return pollutionIndex;
	}

	/**
	 * @param pollutionIndex the pollutionIndex to set
	 */
	public void setPollutionIndex(double pollutionIndex) {
		this.pollutionIndex = pollutionIndex;
	}

	/**
	 * @return the healthIndex
	 */
	public double getHealthIndex() {
		return healthIndex;
	}

	/**
	 * @param healthIndex the healthIndex to set
	 */
	public void setHealthIndex(double healthIndex) {
		this.healthIndex = healthIndex;
	}

	/**
	 * @return the propertyIndex
	 */
	public double getPropertyIndex() {
		return propertyIndex;
	}

	/**
	 * @param propertyIndex the propertyIndex to set
	 */
	public void setPropertyIndex(double propertyIndex) {
		this.propertyIndex = propertyIndex;
	}
}
